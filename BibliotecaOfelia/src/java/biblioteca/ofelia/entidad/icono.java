public class icono {
    
    String idicono;
    String icono;
    String estado;

    public icono() {
    }

    public String getIdicono() {
        return idicono;
    }

    public void setIdicono(String idicono) {
        this.idicono = idicono;
    }

    public String getIcono() {
        return icono;
    }

    public void setIcono(String icono) {
        this.icono = icono;
    }

    public String getEstado() {
        return estado;
    }

    public void setEstado(String estado) {
        this.estado = estado;
    }
    
    
    
}
