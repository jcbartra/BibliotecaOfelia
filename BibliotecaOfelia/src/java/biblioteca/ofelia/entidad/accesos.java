package biblioteca.ofelia.entidad;

/**
 *
 * @author JcBartra
 */
public class accesos {
    
    String idrol;
    String idmenu;
    String idsubmenu;
    String menu;
    String submenu;
    String estado_rol;
    String estado_menu;
    String estado_submenu;

    public accesos() {
    }

    public String getIdrol() {
        return idrol;
    }

    public void setIdrol(String idrol) {
        this.idrol = idrol;
    }

    public String getIdmenu() {
        return idmenu;
    }

    public void setIdmenu(String idmenu) {
        this.idmenu = idmenu;
    }

    public String getIdsubmenu() {
        return idsubmenu;
    }

    public void setIdsubmenu(String idsubmenu) {
        this.idsubmenu = idsubmenu;
    }

    public String getMenu() {
        return menu;
    }

    public void setMenu(String menu) {
        this.menu = menu;
    }

    public String getSubmenu() {
        return submenu;
    }

    public void setSubmenu(String submenu) {
        this.submenu = submenu;
    }

    public String getEstado_rol() {
        return estado_rol;
    }

    public void setEstado_rol(String estado_rol) {
        this.estado_rol = estado_rol;
    }

    public String getEstado_menu() {
        return estado_menu;
    }

    public void setEstado_menu(String estado_menu) {
        this.estado_menu = estado_menu;
    }

    public String getEstado_submenu() {
        return estado_submenu;
    }

    public void setEstado_submenu(String estado_submenu) {
        this.estado_submenu = estado_submenu;
    }
    
    
}
